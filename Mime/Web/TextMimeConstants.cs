using System;

namespace Trivial.Web
{
    /// <summary>
    /// The MIME constants.
    /// </summary>
    public static partial class MimeConstants
    {
        /// <summary>
        /// The popular MIME constants of text and fonts.
        /// </summary>
        public static class Text
        {
            /// <summary>
            /// The MIME content type of plain text.
            /// </summary>
            public const string Plain = "text/plain";

            /// <summary>
            /// The MIME content type of rich text.
            /// </summary>
            public const string Rtf = "text/richtext";

            /// <summary>
            /// The MIME content type of comma-separated values.
            /// </summary>
            public const string Csv = "text/csv";

            /// <summary>
            /// The MIME content type of tab-separated values.
            /// </summary>
            public const string Tsv = "text/tsv";

            /// <summary>
            /// The MIME content type of vCard (Versitcard).
            /// </summary>
            public const string VCard = "text/x-vcard";

            /// <summary>
            /// The MIME content type of diff and patch.
            /// </summary>
            public const string Diff = "text/x-diff";

            /// <summary>
            /// The MIME content type of markdown.
            /// </summary>
            public const string Markdown = "text/markdown";

            /// <summary>
            /// The MIME content type of math markup.
            /// </summary>
            public const string Math = "text/mathml";

            /// <summary>
            /// The MIME content type of Standard Generalized Markup Language.
            /// </summary>
            public const string Sgml = "text/sgml";

            /// <summary>
            /// The MIME content type of ink markup.
            /// </summary>
            public const string Ink = "application/inkml+xml";

            /// <summary>
            /// The MIME content type of iCalendar.
            /// </summary>
            public const string Calendar = "text/calendar";

            /// <summary>
            /// The MIME content type of Microsoft Embedded Open Type.
            /// </summary>
            public const string Eot = "application/vnd.ms-fontobject";

            /// <summary>
            /// The MIME content type of TrueType Font.
            /// </summary>
            public const string TrueType = "font/ttf";

            /// <summary>
            /// The MIME content type of OpenType Layout Font.
            /// </summary>
            public const string OpenType = "font/otf";

            /// <summary>
            /// The MIME content type of Web Open Font Format.
            /// </summary>
            public const string Woff = "font/woff";

            /// <summary>
            /// The MIME content type of Web Open Font Format 2.
            /// </summary>
            public const string Woff2 = "font/woff2";
        }
    }
}
