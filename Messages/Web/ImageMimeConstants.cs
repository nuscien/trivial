using System;

namespace Trivial.Web;

/// <summary>
/// The MIME constants.
/// </summary>
public static partial class MimeConstants
{
    /// <summary>
    /// The popular MIME constants of image.
    /// </summary>
    public static class Images
    {
        /// <summary>
        /// The MIME content type of Animated Portable Network Graphics.
        /// </summary>
        public const string Apng = "image/apng";

        /// <summary>
        /// The MIME content type of Portable Network Graphics.
        /// </summary>
        public const string Png = "image/png";

        /// <summary>
        /// The MIME content type of bitmap (and Device-Independent Bitmap).
        /// </summary>
        public const string Bmp = "image/bmp";

        /// <summary>
        /// The MIME content type of AutoCAD drawings.
        /// </summary>
        public const string Dwg = "image/vnd.dwg";

        /// <summary>
        /// The MIME content type of Graphics Interchange Format.
        /// </summary>
        public const string Gif = "image/gif";

        /// <summary>
        /// The MIME content type of High Efficiency Image Format.
        /// </summary>
        public const string Heif = "image/heif";

        /// <summary>
        /// The MIME content type of High Efficiency Image Codec.
        /// </summary>
        public const string Heic = "image/heic";

        /// <summary>
        /// The MIME content type of icon and cursor.
        /// </summary>
        public const string Ico = "image/x-ico";

        /// <summary>
        /// The MIME content type of Joint Photographic Experts Group.
        /// </summary>
        public const string Jpeg = "image/jpeg";

        /// <summary>
        /// The MIME content type of Adobe Photoshop Document.
        /// </summary>
        public const string Psd = "image/vnd.adobe.photoshop";

        /// <summary>
        /// The MIME content type of Scalable Vector Graphics.
        /// </summary>
        public const string Svg = "image/svg+xml";

        /// <summary>
        /// The MIME content type of WebP (by Google).
        /// </summary>
        public const string Webp = "image/webp";

        /// <summary>
        /// The MIME content type of Windows Meta File.
        /// </summary>
        public const string Wmf = "application/x-msmetafile";
    }
}
